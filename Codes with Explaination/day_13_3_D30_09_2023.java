// v048 Naming Convention in Java --------------------------------------------
public class day_13_3_D30_09_2023 {
    public static void main(String[] args) {
        // Naming conventions in Java are guidelines and conventions for naming various elements in your Java code, such as classes, variables, methods, and packages.
        // Adhering to these conventions makes your code more readable and maintainable, and it also helps improve code consistency when working with others.
        System.out.println("Java Naming Conventions Example:");
        // Packages:
        System.out.println("\nPackages:");
        System.out.println("Package names should be in lowercase letters (e.g., mypackage).");
        System.out.println("Use a reverse domain name as a prefix for package names to ensure uniqueness (e.g., com.example.myapp).\n");
        // Classes and Interfaces:
        System.out.println("Classes and Interfaces:");
        System.out.println("Class and interface names should start with an uppercase letter (e.g., MyClass, MyInterface).");
        System.out.println("Use nouns or noun phrases as class names (e.g., Person, Car, CustomerService).\n");
        // Methods:
        System.out.println("Methods:");
        System.out.println("Method names should start with a lowercase letter (e.g., calculateTotal, printDetails).");
        System.out.println("Use verbs or verb phrases as method names (e.g., calculateTotal, printDetails).\n");
        // Variables:
        System.out.println("Variables:");
        System.out.println("Variable names should start with a lowercase letter (e.g., age, fullName, itemCount).");
        System.out.println("Use meaningful and descriptive variable names.");
        System.out.println("For constants, use all uppercase letters with underscores separating words (e.g., MAX_VALUE, PI).\n");
        // Constants:
        System.out.println("Constants:");
        System.out.println("Constant names should be in uppercase letters with underscores separating words (e.g., MAX_VALUE, PI).\n");
        // Packages, Classes, and Methods:
        System.out.println("Packages, Classes, and Methods:");
        System.out.println("Use camelCase for multi-word names (e.g., calculateTotalPrice, getUserInfo).\n");
        // Acronyms and Abbreviations:
        System.out.println("Acronyms and Abbreviations:");
        System.out.println("Acronyms and abbreviations should be capitalized (e.g., URL, HTTP, XML, GUI).\n");
        // Constructors:
        System.out.println("Constructors:");
        System.out.println("Constructors should have the same name as the class.");
        System.out.println("Use camelCase for constructor parameters (e.g., public Person(String firstName, String lastName)).\n");
        // Boolean Variables and Methods:
        System.out.println("Boolean Variables and Methods:");
        System.out.println("Boolean variable names and methods should often start with \"is,\" \"has,\" or \"can\" followed by a noun or adjective (e.g., isReady, hasPermission, canEdit).\n");
        // Getter and Setter Methods:
        System.out.println("Getter and Setter Methods:");
        System.out.println("Use the \"get\" prefix for getter methods (e.g., getName, getAge).");
        System.out.println("Use the \"set\" prefix for setter methods (e.g., setName, setAge).\n");
        // Enums:
        System.out.println("Enums:");
        System.out.println("Enum types should be in uppercase letters.");
        System.out.println("Enum constants should be in uppercase letters (e.g., DayOfWeek.MONDAY, Color.RED).\n");
        // Annotations:
        System.out.println("Annotations:");
        System.out.println("Annotation types should be in uppercase letters.");
        System.out.println("Annotation members should be named in camelCase (e.g., @Override, @SuppressWarnings(\"deprecation\")).\n");
        // Local Variables:
        System.out.println("Local Variables:");
        System.out.println("Local variable names should be in camelCase and should be meaningful (e.g., int count, String message).\n");
        // Method Parameters:
        System.out.println("Method Parameters:");
        System.out.println("Method parameter names should be in camelCase and should be meaningful (e.g., void processOrder(int orderId)).\n");
        // Indentation and Formatting:
        System.out.println("Indentation and Formatting:");
        System.out.println("Use consistent indentation and formatting throughout your code to improve readability.");
    }
}
